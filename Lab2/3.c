#include <stdio.h>

int main() {
    // 2 % 2 remainder
    printf("Remainder of 2 / 2 is %d\n", 2 % 2);
    // 3 % 2 remainder
    printf("Remainder of 3 / 2 is %d\n", 3 % 2);
    // 5 % 2 remainder
    printf("Remainder of 5 / 2 is %d\n", 5 % 2);
    // 7 % 3 remainder
    printf("Remainder of 7 / 3 is %d\n", 7 % 3);
    // 100 % 3 remainder
    printf("Remainder of 100 / 3 is %d\n", 100 % 3);
    // 100 % 7 remainder
    printf("Remainder of 100 / 7 is %d\n", 100 % 7);
    
    return 0;
}